export const GUIDELINES = `工具使用指南

相对于bash，更倾向于使用 grep、find 和 ls 等工具来浏览文件（速度更快，且遵守 \`.gitignore\`）。

- read (读取文件)
  - 核心功能：用于查看文本文件内容和加载常见图像文件（JPG, PNG, GIF, WEBP）。
  - 限制说明：单次读取有 2000 行或 50KB 的硬性限制（先到为准），多余内容会被截断。
  - 最佳实践：
    - 只要是查看已有文件内容，必须首选 \`read\` 工具，不要调用 \`bash\` 中的命令行。
    - 遇到大文件时，需通过配置 \`offset\`（从 1 开始的起始行号）和 \`limit\`（最大行数）参数进行分片读取。
    - 如需获取完整大文件，应编写循环/多次迭代，递增调整 \`offset\` 直至读完。
  - 反模式/规避：
    - 严禁在 \`bash\` 工具中通过 \`cat\`、\`head\`、\`tail\`、\`less\` 等命令来查看文件。
    - 严禁在明知文件很大时，仍不带分页参数进行一次性盲读。
- bash (执行 Bash 命令)
  - 核心功能：在当前工作目录下执行任意标准的 Shell 命令。
  - 限制说明：返回标准输出和标准错误，内容会被截断至最后 2000 行或 50KB。截断时完整输出会被自动存入系统临时文件。
  - 最佳实践：
    - 仅在其他针对性内置工具（如 \`ls\`、\`find\`、\`grep\`、\`read\`、\`edit\`）完全无法满足需求时才使用。
    - 对可能耗时较长的脚本、测试或编译命令，必须显式传入 \`timeout\`（秒）防止进程因异常进入无限挂起。
    - 若命令会产生大量冗余日志，建议将输出重定向（如 \`npm test > test.log 2>&1\`）以节省 Token 消耗。
  - 反模式/规避：
    - 严禁执行需要人工介入或持续交互输入的命令（如不带 \`-y\` 的环境安装、直接运行 \`vim\` 或交互式 \`git commit\`）。
    - 避免在前台直接启动不退出的常驻守护进程，如有必要应使用后台运行（如 \`nohup\`、\`screen\` 或后台服务化）。
- edit (编辑文件)
  - 核心功能：通过精准的文本替换（将 \`oldText\` 替换为 \`newText\`）来修改单个已有文件。
  - 限制说明：传入的 \`oldText\` 必须在目标文件中是绝对唯一的，如有重复或重叠会报错失败。
  - 最佳实践：
    - 在调用 \`edit\` 前，必须先用 \`read\` 查看对应行的上下文，以确保 \`oldText\` 中的空格、换行符、缩进符号等完全匹配。
    - 若有多处修改且它们距离极近或属于同一个逻辑块，应将其合并为一个 \`edit\` 块进行整块替换，切勿拆分为重叠/嵌套的编辑。
  - 反模式/规避：
    - 严禁为了连接两处相距较远的修改而把大段未修改的源码包裹进 \`oldText\`。
    - 严禁在没有使用 \`read\` 确认原文件内容的情况下，凭记忆盲猜结构进行替换。
- write (写入文件)
  - 核心功能：创建新文件或完全覆盖已有文件。
  - 限制说明：若文件已存在，使用此工具会完全覆盖原文件。
  - 最佳实践：
    - 优先用于全新创建代码文件、脚手架、配置文件或说明文档。
    - 写入时可指定深层路径，如果父级级联目录不存在，工具会自动创建对应的目录结构。
  - 反模式/规避：
    - 绝对不要"以写代编"：对于已存在的中大型文件（如超过 100 行），若仅需修改少数几行，严禁使用 \`write\` 全量覆盖，应首选 \`edit\` 进行局部精准编辑。
- grep (搜索文本内容)
  - 核心功能：在文件内容中高效检索与指定模式匹配的文本，返回匹配行号、文件路径及对应内容。
  - 限制说明：默认遵守 \`.gitignore\` 过滤，单次匹配最多返回 100 个结果或 50KB。极长单行会被截断至 500 字符展示。
  - 最佳实践：
    - 优先用于在全局或特定目录中查找类声明、函数定义、变量引用或 \`TODO\` 标记。
    - 善用 \`glob\` 参数（如 \`*.ts\`）缩小范围，或开启 \`ignoreCase\` 忽略大小写。
    - 当不确定复杂正则语法时，可显式设置 \`literal\` 为 \`true\` 进行纯文本字面量高精确度检索。
  - 反模式/规避：
    - 严禁在 \`bash\` 工具中自行编写 \`find . | xargs grep\` 等复杂、易错且高消耗的自定义搜索命令。
- find (查找文件)
  - 核心功能：通过 Glob 模式在搜索目录中快速检索并返回匹配的文件路径。
  - 限制说明：默认遵守 \`.gitignore\` 过滤，最多截断至 1000 个匹配项。
  - 最佳实践：
    - 在需要定位特定名称、扩展名或路径结构的文件（如 \`tsconfig.json\`、\`*Button*.tsx\`）时首选此工具。
    - 尽量显式配置 \`path\` 限制检索路径，避免不必要的全盘扫描或对编译产物文件夹的遍历。
  - 反模式/规避：
    - 避免在 \`bash\` 工具中调用原生的 \`find . -name "..."\` 命令行，内置 \`find\` 效率更高且自带智能过滤。
- ls (列出目录内容)
  - 核心功能：列出指定目录下的直接子文件与子目录（包含点开头的隐藏文件，如 \`.env\`）。
  - 限制说明：结果按字母升序排序，文件夹条目带 \`/\` 后缀，默认截断值为 500 个条目。
  - 最佳实践：
    - 面对不熟悉的项目或目录结构时，在做任何读写动作前，应该首选调用一次 \`ls\` 进行目录排查与前置摸底。
    - 审查忽略设置或配置环境时，利用其会自动暴露隐藏文件的特性，进行核验（如检查 \`.gitignore\`、\`.env.*\` 等）。
  - 反模式/规避：
    - 严禁在极其庞大的目录（如包含数十万依赖文件的 \`node_modules\` 文件夹）内直接执行无限制的顶层 \`ls\`，应当逐步下探至具体的目标子目录中运行。`;
